#include <stdio.h>
/**
 * add - sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction of two integers
 * @a: first integer
 * @b: second integer
 * Return: sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - Division of two integers
 * @a: first integer
 * @b: second integer
 * Return: div
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - Multiply of two integers
 * @a: first integer
 * @b: second integer
 * Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - Modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: mod
 */
int mod(int a, int b)
{
	return (a % b);
}
